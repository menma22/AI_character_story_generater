def wrap_context(section_name: str, data: str, agent_role: str = "default") -> str:
    """コンテキストデータに説明を付与してラッピングする。

    Args:
        section_name: セクション名（例: "マクロプロフィール"）
        data: 実際のコンテキストデータ（JSON文字列やテキスト）
        agent_role: エージェントの役割（例: "impulsive", "diary"）。
                    該当するロール固有の説明がなければ "default" にフォールバック。

    Returns:
        説明付きのコンテキスト文字列
    """
    descs = CONTEXT_DESCRIPTIONS.get(section_name, {})
    desc = descs.get(agent_role, descs.get("default"))

    if not desc:
        return f"【{section_name}】\n{data}"

    what, why, how = desc
    header = f"【{section_name}】\n※ 何: {what}\n※ 目的: {why}\n※ 使い方: {how}"
    return f"{header}\n\n{data}"
